$("#B").click(function(){
  setInterval(function(){
      $("#B").css("border-top-style", "solid");
  }, 1000);
  setInterval(function(){
      $("#B").css("border-right-style", "solid");
  }, 2000);
  setInterval(function(){
      $("#B").css("border-bottom-style", "double");
  }, 1000);
  setInterval(function(){
      $("#B").css("border-left-style", "double");
  }, 2000);
});
